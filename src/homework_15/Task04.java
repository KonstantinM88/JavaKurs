package homework_15;
/*
Используйте оператор switch для решения задачи:

Ваш ребенок принес оценку за контрольную работу по математике. В школе 12-балльная система. Запишите в переменную случайное число от 0 до 12.

Вы разрешаете ребенку смотреть телевизор не более 60 минут в день. На сегодня у него осталось 45 минут.

Если оценка от 10 до 12 баллов: скажите ребенку, как вы рады, и добавьте к оставшемуся времени 1 час.
Оценка от 7 до 9 баллов: похвалите ребенка и добавьте 45 минут.
Оценка от 4 до 6 баллов: нейтрально отнеситесь и добавьте 15 минут.
Оценка 3 балла: огорчитесь и вычтите 30 минут.
Оценка меньше 3 баллов: запретите телевизор на сегодня.
Выведите на экран оставшееся время для просмотра ТВ.

P.S. Проконтролируйте, чтобы ребенок не смотрел ТВ больше 60 минут в день.

 */
import java.util.Random;

public class Task04 {
    public static void main(String[] args) {


        // Генерация случайной оценки от 0 до 12
        Random random = new Random();
        int grade = random.nextInt(13);  // Оценка от 0 до 12
        // int grade = 3;
        // Начальное оставшееся время (45 минут)
        int startTime = 45;

        // Используем switch для обработки оценки
        switch (grade) {
            case 10:
            case 11:
            case 12:
                // Оценка от 10 до 12 баллов: добавляем 1 час (60 минут)
                int a = 60;
                System.out.println("Молодец! Ты получил " + grade + " баллов. Ты получил дополнительно " + a + " мин для просмотра ТВ");
                startTime += a;
                break;

            case 7:
            case 8:
            case 9:
                // Оценка от 7 до 9 баллов: добавляем 45 минут
                int b = 45;
                System.out.println("Хорошо! Ты получил " + grade + " баллов. Ты получил дополнительно " + b + " мин для просмотра ТВ" );
                startTime += b;
                break;

            case 4:
            case 5:
            case 6:
                // Оценка от 4 до 6 баллов: добавляем 15 минут
                int c = 15;
                System.out.println("Нормально, но есть над чем работать. Ты получил " + grade + " баллов. Ты получил дополнительно " + c + " мин для просмотра ТВ " );
                startTime += c;
                break;

            case 3:
                // Оценка 3 балла: вычитаем 30 минут
                int e = 30;
                startTime -= e;
                System.out.println("Огорчение, ты получил " + grade + " баллов. Ты наказан и потерял " + e + " мин для просмотра ТВ");
                break;

            default:
                // Оценка меньше 3 баллов: запретить телевизор
                System.out.println("Ты получил " + grade + " баллов. Телевизор сегодня не смотришь.");
                startTime = 0;
                break;
        }

        // Проверяем, чтобы время не превышало 60 минут
        if (startTime > 60) {
            startTime = 60;
        }

        // Выводим оставшееся время для просмотра ТВ
        System.out.println("Оставшееся время для просмотра ТВ: " + startTime + " минут.");

    }
}
