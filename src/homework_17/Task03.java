package homework_17;
/*
Task 3
Написать метод, принимающий на вход целое число. Метод должен вывести на экран 2 в степени этоЧисло

Например: Если в метод пришло число 3 - метод должен вывести число 8 (так как 2 в степени 3 = 8)

класс Math для нахождения степени числа использовать нельзя ;)

P.S. А почему вы решили, что число может прийти только положительное? ;)
 */


public class Task03 {


        // Метод для вывода 2 в степени number
        public static void printTwo(int number) {
            // Для случая, если степень равна 0, результат всегда 1
            if (number == 0) {
                System.out.println(1);
                return;
            }

            // Для положительных степеней
            if (number > 0) {
                int result = 1;  // Начинаем с 1, так как 2^0 = 1
                for (int i = 0; i < number; i++) {
                    result *= 2;  // Умножаем на 2 столько раз, сколько указано в числе
                }
                System.out.println(result);
            }
            // Для отрицательных степеней
            else {
                double result = 1.0;  // Начинаем с 1.0
                for (int i = 0; i < Math.abs(number); i++) {
                    result /= 2;  // Разделяем на 2 столько раз, сколько указано в числе
                }
                System.out.println(result);
            }
        }

        public static void main(String[] args) {
            // Пример вызова метода с различными значениями числа
            printTwo(3);  // Вывод: 8
            printTwo(0);  // Вывод: 1
            printTwo(-3); // Вывод: 0.125
        }


}
